#include <stdio.h>

int	ft_atoi(char	*str)
{
	int	a;
	int	b;
	int	c;

	a = 0;
	b = 1;
	c = 0;
	while (str[a] == '\t' || str[a] == '\n' || str[a] == ' '
		|| str[a] == '\v' || str[a] == '\f' || str[a] == '\r')
		a++;
	while (str[a] == '+' || str[a] == '-')
	{
		if (str[a] == '-')
			b = b * -1;
		a++;
	}
	while (str[a] >= '0' && str[a] <= '9')
	{
		c = (str[a] - '0') + (c * 10);
		a++;
	}
	return (c * b);
}
