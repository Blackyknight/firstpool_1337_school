#include <unistd.h>

int	main(int	ac, char	**av)
{
	int	a;

	a = 0;
	if (ac > 1)
	{
		ac = ac - 1;
		while (ac > 0)
		{
			a = 0;
			while (av[ac][a] != '\0')
			{
				write(1, &av[ac][a], 1);
				a++;
			}
			write(1, "\n", 1);
			ac--;
		}
	}
	return (0);
}
